    private void btFinalizarCadastroProfissionalActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btFinalizarCadastroProfissionalActionPerformed
        // TODO add your handling code here:
        if (tfProfissionalNome.getText().isEmpty()) {
            JOptionPane.showMessageDialog(pnCadastrarNovoProfissional, "Por favor insira o nome do profissional", "Aviso", 2);
            tfProfissionalNome.requestFocus();
        } else {
            if (tfProfissionalProfissao.getText().isEmpty()) {
                JOptionPane.showMessageDialog(pnCadastrarNovoProfissional, "Por favor insira a profissão do profissional", "Aviso", 2);
                tfProfissionalNome.requestFocus();
            } else {
                if (tfProfissionalCpf.getText().isEmpty()) {
                    JOptionPane.showMessageDialog(pnCadastrarNovoProfissional, "Por favor insira o cpf do profissional", "Aviso", 2);
                    tfProfissionalCpf.requestFocus();
                } else {
                    if (btFinalizarCadastroProfissional.getToolTipText().equals("Cadastrar")) {
                        if (tfProfissionalCpf.getValue() != null) {
                            st = conexaoTabelaProfissionais.pesquisarCpf(0, tfProfissionalCpf.getText());
                            if (st == 1) {
                                JOptionPane.showMessageDialog(pnCadastrarNovoProfissional, "O cpf informado já existe!", "Aviso", 2);
                                tfProfissionalCpf.requestFocus();
                                return;
                            }
                        }

                    } else {
                        Profissional cliente = modeloTabelaProfissional.retornaListaProfissionais().get(tbProfissionaisCadastrados.getSelectedRow());
                        if ((tfProfissionalCpf.getValue() != null)) {
                            st = conexaoTabelaProfissionais.pesquisarCpf(cliente.getProfissional_id(), tfProfissionalCpf.getText());
                            if (st == 1) {
                                JOptionPane.showMessageDialog(pnCadastrarNovoProfissional, "O cpf informado já existe!", "Aviso", 2);
                                tfProfissionalCpf.requestFocus();
                                return;
                            }
                        }
                    }
                    if (tfProfissionalCrea.getText().isEmpty()) {
                        JOptionPane.showMessageDialog(pnCadastrarNovoProfissional, "Por favor insira o crea do profissional", "Aviso", 2);
                        tfProfissionalCrea.requestFocus();
                    } else {
                        if (tfProfissionalCtma.getText().isEmpty()) {
                            JOptionPane.showMessageDialog(pnCadastrarNovoProfissional, "Por favor insira o ctma do profissional", "Aviso", 2);
                            tfProfissionalCtma.requestFocus();
                        } else {
                            if (tfProfissionalCep.getText().isEmpty()) {
                                JOptionPane.showMessageDialog(pnCadastrarNovoProfissional, "Por favor insira o cep do profissional", "Aviso", 2);
                                tfProfissionalCep.requestFocus();
                            } else {
                                if (tfProfissionalRua.getText().isEmpty()) {
                                    JOptionPane.showMessageDialog(pnCadastrarNovoProfissional, "Por favor insira a rua do profissional", "Aviso", 2);
                                    tfProfissionalRua.requestFocus();
                                } else {
                                    if (tfProfissionalBairro.getText().isEmpty()) {
                                        JOptionPane.showMessageDialog(pnCadastrarNovoProfissional, "Por favor insira o bairro do profissional", "Aviso", 2);
                                        tfProfissionalBairro.requestFocus();
                                    } else {
                                        if (tfProfissionalCidade.getText().isEmpty()) {
                                            JOptionPane.showMessageDialog(pnCadastrarNovoProfissional, "Por favor insira a cidade do profisisonal", "Aviso", 2);
                                            tfProfissionalCidade.requestFocus();
                                        } else {

                                            if (cbProfissionalEstado.getSelectedIndex() == 0) {
                                                JOptionPane.showMessageDialog(pnCadastrarNovoProfissional, "Por favor insira o estado do profissional", "Aviso", 2);
                                                cbProfissionalEstado.requestFocus();
                                            } else {
                                                if (btFinalizarCadastroProfissional.getToolTipText().equals("Cadastrar")) {
                                                    conexaoTabelaProfissionais.inserirNovoProfissional(preencherDadosCadastroProfissional());
                                                    buscarProfissionaisTabela();
                                                    limparCamposCadastroProfissional();
                                                } else {
                                                    conexaoTabelaProfissionais.alterarProfissional(modeloTabelaProfissional.retornaListaProfissionais().get(tbProfissionaisCadastrados.getSelectedRow()).getProfissional_id(), preencherDadosCadastroProfissional());

                                                    buscarProfissionaisTabela();

                                                    limparCamposCadastroProfissional();
                                                }
                                            }
                                        }
                                    }
                                }
                            }
                        }
                    }
                }

            }
        }


    }//GEN-LAST:event_btFinalizarCadastroProfissionalActionPerformed

