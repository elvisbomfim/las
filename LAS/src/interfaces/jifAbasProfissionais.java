    private void tfPalavraChaveProfissionalKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_tfPalavraChaveProfissionalKeyReleased
        // TODO add your handling code here:
        if (tfPalavraChaveProfissional.getText().isEmpty()) {
            modeloTabelaProfissional.inserirListaProfissionais(conexaoTabelaProfissionais.selecionarTodosProfissionais());
        } else {

            modeloTabelaProfissional.inserirListaProfissionais(conexaoTabelaProfissionais.selecionarTodosProfissionais());
            listaOriginalTemporariaProfissional.clear();

            if (ckbProfissionalCpf.isSelected()) {
                for (int i = 0; i < modeloTabelaProfissional.retornaListaProfissionais().size(); i++) {
                    if (modeloTabelaProfissional.retornaListaProfissionais().get(i).getProfissional_cpf().toLowerCase().contains(tfPalavraChaveProfissional.getText().toLowerCase())) {
                        listaOriginalTemporariaProfissional.add(modeloTabelaProfissional.retornaListaProfissionais().get(i));
                    }
                }
            } else if (ckbProfissionalNome.isSelected()) {
                for (int i = 0; i < modeloTabelaProfissional.retornaListaProfissionais().size(); i++) {
                    if (modeloTabelaProfissional.retornaListaProfissionais().get(i).getProfissional_nome().toLowerCase().contains(tfPalavraChaveProfissional.getText().toLowerCase())) {
                        listaOriginalTemporariaProfissional.add(modeloTabelaProfissional.retornaListaProfissionais().get(i));
                    }
                }
            }

            modeloTabelaProfissional.inserirListaProfissionais(listaOriginalTemporariaProfissional);

        }

        tbProfissionaisCadastrados.updateUI();


    }//GEN-LAST:event_tfPalavraChaveProfissionalKeyReleased

    private void btExcluirProfissionalActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btExcluirProfissionalActionPerformed
        // TODO add your handling code here:
        if (tbProfissionaisCadastrados.getSelectedRow() == -1) {
            JOptionPane.showMessageDialog(rootPane, "Não existem clientes selecionados para serem deletados");
        } else if (tbProfissionaisCadastrados.getRowCount() == 0) {
            JOptionPane.showMessageDialog(rootPane, "Não existem mais clientes para serem deletados");
        } else if (JOptionPane.showConfirmDialog(rootPane, "Deseja realmente excluir esse cliente?", "Excluir cliente", 0) == 0) {
            if (tbProfissionaisCadastrados.getSelectedRow() != -1) {
                conexaoTabelaProfissionais.removerProfissional(modeloTabelaProfissional.retornaListaProfissionais().get(tbProfissionaisCadastrados.getSelectedRow()).getProfissional_id(), tbProfissionaisCadastrados.getValueAt(tbProfissionaisCadastrados.getSelectedRow(), 0).toString());
                modeloTabelaProfissional.inserirListaProfissionais(conexaoTabelaProfissionais.selecionarTodosProfissionais());

                tbProfissionaisCadastrados.updateUI();
            }
        }
    }//GEN-LAST:event_btExcluirProfissionalActionPerformed

